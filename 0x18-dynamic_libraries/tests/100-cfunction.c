/**
 *add - adds two parametrs.
 *@num1: the first number
 *@num2: the Second number
 *Return: sum
 */
int add(int num1, int num2)
{
	return (num1 + num2);
}

/**
 *sub - performs Substraction two parametrs.
 *@num1: the first number
 *@num2: the Second number
 *Return: Difference
 */
int sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 *mul - Multiplies two parametrs.
 *@num1: the first number
 *@num2: the Second number
 *Return: Product
 */
int mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 *div - adds two parametrs.
 *@num1: the first number
 *@num2: the Second number
 *Return: questiont
 */
int div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 *mod - calculates remainder.
 *@num1: the first number
 *@num2: the Second number
 *Return: remainder
 */
int mod(int num1, int num2)
{
	return (num1 % num2);
}
